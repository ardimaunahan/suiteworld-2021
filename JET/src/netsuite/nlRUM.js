// Use various techniques to determine the time at which this page started.
NLRUM.findStartTime = function() {
    var startTime = NLRUM.findStartWebTiming();//// || NLRUM.findStartCookie();
    if ( startTime ) {
        NLRUM.mark("starttime", startTime);
    }
};

// Find the start time from the Web Timing "performance" object.
NLRUM.findStartWebTiming = function() {
    var startTime = undefined;
    var performance = NLRUM.getPerformanceObject();

	if ( typeof(performance) !== "undefined" && typeof(performance.timing) !== "undefined" && typeof(performance.timing["navigationStart"]) !== "undefined" ) {
        startTime = performance.timing["navigationStart"];
        NLRUM.dprint("NLRUM.findStartWebTiming: startTime = " + startTime);
    }

    return startTime;
};


// Find the start time based on a cookie set by NLRUM in the unload handler.
NLRUM.findStartCookie = function() {
    var aCookies = document.cookie.split(' ');
    for ( var i = 0; i < aCookies.length; i++ ) {
        if ( 0 === aCookies[i].indexOf("NLRUM=") ) {
            var aSubCookies = aCookies[i].substring("NLRUM=".length).split('&');
            var startTime, bReferrerMatch;
            for ( var j = 0; j < aSubCookies.length; j++ ) {
                if ( 0 === aSubCookies[j].indexOf("s=") ) {
                    startTime = aSubCookies[j].substring(2);
                }
                else if ( 0 === aSubCookies[j].indexOf("r=") ) {
                    var startPage = aSubCookies[j].substring(2);
                    bReferrerMatch = ( escape(document.referrer) == startPage );
                }
            }
            if ( bReferrerMatch && startTime ) {
                NLRUM.dprint("NLRUM.findStartCookie: startTime = " + startTime);
                return startTime;
            }
        }
    }
    return undefined;
};

NLRUM.beforeUnload = function(e) {
	var jsonParams = {};
	//ResourceTime
	var resrcTimingArray = NLRUM.getResourceTiming();
	if (NLRUM.bResourceTimingDataCollection > 0 && resrcTimingArray.length > 0) {
		jsonParams['resourcetime'] = resrcTimingArray;
		NLRUM.clearResourceTimings();
	}
	//Client Script
	if (NLRUM.clientScriptTime.length !== 0) {
		jsonParams['clientscripttime'] = NLRUM.clientScriptTime;
		NLRUM.clientScriptTime = [];
	}
	if (!jsonParams.resourcetime && !jsonParams.clientscripttime) {
		return;
	}
	var data = JSON.stringify(jsonParams);
	//Use Beacon API if possible
	if (navigator.sendBeacon) {
		navigator.sendBeacon(NLRUM.beaconUrl, data);
	}
	else {
		var xmlHttpReq = (window.XMLHttpRequest)  ? new XMLHttpRequest() : (window.ActiveXObject)  ? new ActiveXObject("Microsoft.XMLHTTP") : null;
		if (xmlHttpReq) {
			xmlHttpReq.open('POST', NLRUM.beaconUrl, false);
			xmlHttpReq.setRequestHeader('Content-Type', 'application/json');
			xmlHttpReq.send(data);
		}
	}
};

