NLRUM.getNavigationTiming = function() {
    var pn, pt, navTimeData = {} ;
    var p = NLRUM.getPerformanceObject();

	if(p && p.timing) {
        NLRUM.dprint("This user agent supports NavigationTiming.", "nt");
        pt = p.timing;
        navTimeData = {
			//Perfomance.timing
			connEnd: Math.floor(pt.connectEnd),
			connStart: Math.floor(pt.connectStart),
			domComplete: Math.floor(pt.domComplete),
			domContentLoadedEvtEnd: Math.floor(pt.domContentLoadedEventEnd),
			domContentLoadedEvtStart: Math.floor(pt.domContentLoadedEventStart),
			domInteractive: Math.floor(pt.domInteractive),
			domloading: Math.floor(pt.domLoading),
			dmnLkEnd: Math.floor(pt.domainLookupEnd),
			dmnLkStart: Math.floor(pt.domainLookupStart),
			browsertime_fetchStart: Math.floor(pt.fetchStart),
			loadEvtEnd: Math.floor(pt.loadEventEnd),
			loadEvtStart: Math.floor(pt.loadEventStart),
			navStart: Math.floor(pt.navigationStart),
			redirEnd: Math.floor(pt.redirectEnd),
			redirStart: Math.floor(pt.redirectStart),
			reqStart: Math.floor(pt.requestStart),
			respEnd: Math.floor(pt.responseEnd),
			respStart: Math.floor(pt.responseStart),
			unloadEvtEnd: Math.floor(pt.unloadEventEnd),
			unloadEvtStart: Math.floor(pt.unloadEventStart)
        };

		//Optional nav/timings/buckets
		if (window.PerformanceNavigationTiming) {
			var pn = p.getEntriesByType('navigation')[0];
			if (pn) {
				navTimeData['navigationType'] = pn.type;
				navTimeData['redirCt'] = pn.redirectCount;
			}
		}
		if (window.PerformancePaintTiming) {
			var r = p.getEntriesByType('paint')[0];
			if (r && r.startTime) {
				navTimeData['firstPaint'] = Math.floor(r.startTime + pt.navigationStart);
			}
		}
		else if (pt.msFirstPaint > 0) {
			navTimeData['firstPaint'] = Number(pt.msFirstPaint);
		}

		//Optional timings/buckets
		if (pt.secureConnectionStart) {
            // secureConnectionStart is OPTIONAL in the spec
			navTimeData['secureConnStart'] = Math.floor(pt.secureConnectionStart);
        }
        NLRUM.addESFieldsForBrowserTime(navTimeData);
    }
    return navTimeData;
};

