NLRUM.addSCData = function(params)
{
    var url = NLRUM.beaconUrl || '/app/performance/NLRUMRequestHandler.nl';
    if(url.indexOf("?") > -1)
        url = url + "&sc=T";
    else
        url = url + "?sc=T";
    NLRUM.firstLoad = 0; //ignore firstLoad flag, always send browser data with SC data
    NLRUM.sendPerfData(url, params);
};

NLRUM.sendPerfData = function(url, customParams) {
    url = url || NLRUM.beaconUrl;
    var perfData = {
	    pgMeasures : NLRUM.mergeObjects(NLRUM.getMeasures(), NLRUM.getMarks()) //send marks to server too
    };
    //POST Data to server
    var jsonParams = {};


	if(!(NLRUM.firstLoad) ){///send navigation data once only
		///Merge the navigation timing and page measures for the initial page load...
		perfData.pgMeasures = NLRUM.mergeObjects(perfData.pgMeasures , NLRUM.getNavigationTiming());
		jsonParams['browsertime'] = perfData.pgMeasures;
	}

	if(NLRUM.bResourceTimingDataCollection > 0)
	{
        var resrcTimingArray = NLRUM.getResourceTiming();
		if(resrcTimingArray.length > 0) {
			jsonParams['resourcetime'] = resrcTimingArray;
        }
    }

    if(customParams)
        jsonParams['customParams'] = customParams;

	NLRUM.firstLoad = 1;
	NLRUM.isFirstLoad = false;
	////Clear out the resource times, measures
	NLRUM.clearResourceTimings();
	NLRUM.clearMeasures();
	NLRUM.sendData(url, jsonParams);
};

NLRUM.getXMLHttpReqObject = function(beaconUrl) {
    var xmlHttpReq = (window.XMLHttpRequest)  ? new XMLHttpRequest() : (window.ActiveXObject)  ? new ActiveXObject("Microsoft.XMLHTTP") : null;
    xmlHttpReq.open('POST', beaconUrl, true);
    xmlHttpReq.setRequestHeader('Content-Type', 'application/json');
    //xmlHttpReq.timeout = NLRUM.beaconUrlTimeout || 10000;
    return xmlHttpReq;
};

